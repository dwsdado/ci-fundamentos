	<h3> A origem </h3>
		<p>
		Ainda não se sabe exatamente quando apareceu o skate, mas podemos dizer que foi no princípio dos anos 60 na Califórnia. Era em uma época aonde reinava o surf e a curtição total sobre uma prancha, mas como as coisas nunca davam certo aqueles mesmos surfistas pegaram as rodas de seus patins, e colocaran em "shapes", para que assim pudessem surfar em terra firme.

<br>	<br>Os skates eram muito primitivos, não possuiam nose nem tail, era apenas uma tábua e quatro rodinhas. O crescimento do 'surf no asfalto' se deu de uma maneira tão grande que muitos dos jovens da época se renderam ao novo esporte chamado skate. Surgiam então os primeiros skatistas da época. 

<br> 	<br>Era uma época onde o free style dominava, os skatistas usavam e abusavam deste tipo de manobra. No ano de 1965 se comercializaram os primeiros skates fabricados industrialmente e começaram as primeiras competições. Esse esporte então teve seu auge em meados dos anos 70, quando ocorreu um fato que chocou a maior parte de todos os skatistas: a revista "Skateboarder", uma das mais importantes sobre o assunto, anunciou a sua mudança de planos, agora cobrindo assuntos sobre competicões de Biker's. 

Foi quando se deu a 'morte' do skate. Muitas pistas fecharam, e muitos abandonaram o esporte, apenas ficando os que realmente gostavam. Esses skatistas - que perderam suas pistas, suas revistas, e tudo que era a respeito deles - lançaram-se a andar nas ruas, usando tudo que achavam no cotidiano como obstáculo. Daí se deu o street skate.
